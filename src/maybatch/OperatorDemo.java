package maybatch;

public class OperatorDemo {
	public static void main(String[] args) {
		// Arithmatic operator
//		+
//		-
//		*
//		/
//		%
		
		int i = 10;
		int j = 7;
		
		
		System.out.println(i+j); // 17
		System.out.println(i-j); // 3
		System.out.println(i*j); // 70
		System.out.println((float)i/(float)j); // 1.4
		System.out.println(i%j); // 3

		
		// odd % 2 = 1
		// even % 2 = 0

		// 
		
		
	}
}
